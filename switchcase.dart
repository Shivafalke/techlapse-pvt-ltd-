void main() {

	// Switch Case Statements: Applicable for only 'int' and 'String'

	int marks = 90;

	switch (marks) {

		case 90:
			print("Excellent grade of A");
			break;

		case 80:
			print("Very Good !");
			break;

		case 70:
			print("Good enough. But work hard");
			break;

		case 60:
			print("You have failed");
      case 50:
      print("fail");
			break;
		default:
			print("Invalid Grade");
	}
}